const P = (text, translation_ko) => ({ sentences: [{ text, translation_ko }] });
const Q = (question_ko, options, answerIndex, explain_ko) => ({
  question_ko,
  type: "mc",
  options,
  answerIndex,
  explain_ko,
});

const countWords = (pages) =>
  pages.reduce(
    (sum, page) =>
      sum +
      page.sentences.reduce(
        (s, sentence) => s + (sentence.text.match(/[A-Za-z']+/g) ?? []).length,
        0,
      ),
    0,
  );

const quiz = ({ title_ko, main, place, challenge, helper, lesson }) => [
  Q(`${title_ko}에서 중심이 되는 것은 무엇인가요?`, [main, "낡은 시계", "빨간 버스", "큰 가방"], 0, `${main}이 이야기의 중심이에요.`),
  Q("이야기는 주로 어디에서 펼쳐지나요?", [place, "병원", "기차역", "운동장"], 0, `${place}에서 중요한 일이 일어나요.`),
  Q("주인공이 만난 문제는 무엇인가요?", [challenge, "간식이 없었어요", "비밀 편지를 잃었어요", "문이 잠겼어요"], 0, challenge),
  Q("누가 또는 무엇이 도움을 주었나요?", [helper, "검은 구름", "빈 상자", "낯선 모자"], 0, `${helper}이 해결을 도와요.`),
  Q("이야기가 알려 주는 마음은 무엇인가요?", [lesson, "빨리 이기는 마음", "혼자 숨는 마음", "아무것도 하지 않는 마음"], 0, `${lesson}을 배워요.`),
];

const B = (book) => ({
  ...book,
  pages: book.lines.map(([text, ko]) => P(text, ko)),
  quiz: quiz(book.quizFacts),
});

